# Multiplicação: *
# Divisão: /
# Módulo: %
# Maior que: >
# Menor que: <
# Maior ou igual que: >=
# Menor ou igual que: <=
# Igual a: ==
# Diferente de: !=


numero1 = 10
numero2 = 20

print(numero1 + numero2)

print(numero1 - numero2)

print(numero1 * numero2)

print(numero1 / numero2)

print (numero1//numero2)

print(numero1 % numero2)

print(numero1 > numero2)

print(numero1 < numero2)

print(numero1 >= numero2)

print(numero1 <= numero2)

print(numero1 == numero2)

print(numero1 != numero2)

print( 20 // 3)

print(2 % 3)

print(2 ** 3)


